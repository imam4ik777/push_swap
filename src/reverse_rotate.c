#include "push_swap.h"

static void	rev_rotate(t_stack **stack)
{
	t_stack	*tmp = *stack;
	t_stack	*tail = get_stack_bottom(*stack);
	t_stack	*before_tail = get_stack_before_bottom(*stack);

	*stack = tail;
	tail->next = tmp;
	before_tail->next = NULL;
}

void	do_rra(t_stack **stack_a)
{
	rev_rotate(stack_a);
	ft_putstr("rra\n");
}

void	do_rrb(t_stack **stack_b)
{
	rev_rotate(stack_b);
	ft_putstr("rrb\n");
}

void	do_rrr(t_stack **stack_a, t_stack **stack_b)
{
	rev_rotate(stack_a);
	rev_rotate(stack_b);
	ft_putstr("rrr\n");
}
